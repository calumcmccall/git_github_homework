word_1 = "A"
word_2 = "small"
word_3 = "tale"

def sentence(first_word, second_word, third_word):
    sentence = first_word + " " + second_word + " " + third_word
    print(sentence)

sentence(word_1, word_2, word_3)

def uppercase(word):
    uppercase_word = word.upper
    print(uppercase_word)

uppercase(word_1)

